% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

    % Start by grabbing all the inputs
    guess = [ str2double(get(handles.g_R_R,'String')),...
          str2double(get(handles.g_RQ1_Yq,'String')),...
          str2double(get(handles.g_RQ1_nq,'String')),...
          str2double(get(handles.g_RQ1_R,'String')),...
          str2double(get(handles.g_RQ2_Yq,'String')),...
          str2double(get(handles.g_RQ1_nq,'String')),...
          str2double(get(handles.g_RQ1_R,'String')),...
          str2double(get(handles.g_GE_Tc,'String')),...
          str2double(get(handles.g_GE_R,'String')),...
          str2double(get(handles.g_FLW_Tw,'String')),...
          str2double(get(handles.g_FLW_nw,'String')),...
          str2double(get(handles.g_FLW_R,'String'))        ];
    ubs =   [ str2double(get(handles.ub_R_R,'String')),...
          str2double(get(handles.ub_RQ1_Yq,'String')),...
          str2double(get(handles.ub_RQ1_nq,'String')),...
          str2double(get(handles.ub_RQ1_R,'String')),...
          str2double(get(handles.ub_RQ2_Yq,'String')),...
          str2double(get(handles.ub_RQ1_nq,'String')),...
          str2double(get(handles.ub_RQ1_R,'String')),...
          str2double(get(handles.ub_GE_Tc,'String')),...
          str2double(get(handles.ub_GE_R,'String')),...
          str2double(get(handles.ub_FLW_Tw,'String')),...
          str2double(get(handles.ub_FLW_nw,'String')),...
          str2double(get(handles.ub_FLW_R,'String'))        ];
    lbs =   [ str2double(get(handles.lb_R_R,'String')),...
          str2double(get(handles.lb_RQ1_Yq,'String')),...
          str2double(get(handles.lb_RQ1_nq,'String')),...
          str2double(get(handles.lb_RQ1_R,'String')),...
          str2double(get(handles.lb_RQ2_Yq,'String')),...
          str2double(get(handles.lb_RQ1_nq,'String')),...
          str2double(get(handles.lb_RQ1_R,'String')),...
          str2double(get(handles.lb_GE_Tc,'String')),...
          str2double(get(handles.lb_GE_R,'String')),...
          str2double(get(handles.lb_FLW_Tw,'String')),...
          str2double(get(handles.lb_FLW_nw,'String')),...
          str2double(get(handles.lb_FLW_R,'String'))        ];
    
    % run the fitting function
    [fit, err] = fit_eis_dat('exp_data_fine.csv',guess,ubs,lbs);
    
    % display the output data
    fit = strsplit(num2str(fit,2));
    err = num2str(err*100,2); %multiply by 100 to convert to percent
    set(handles.out_error,'String',err)
    set(handles.out_R_R,'String',cell2mat(fit(1)));
    set(handles.out_RQ1_Yq,'String',cell2mat(fit(2)));
    set(handles.out_RQ1_nq,'String',cell2mat(fit(3)));
    set(handles.out_RQ1_R,'String',cell2mat(fit(4)));
    set(handles.out_RQ2_Yq,'String',cell2mat(fit(5)));
    set(handles.out_RQ2_nq,'String',cell2mat(fit(6)));
    set(handles.out_RQ2_R,'String',cell2mat(fit(7)));
    set(handles.out_GE_Tc,'String',cell2mat(fit(8)));
    set(handles.out_GE_R,'String',cell2mat(fit(9)));
    set(handles.out_FLW_Tw,'String',cell2mat(fit(10)));
    set(handles.out_FLW_nw,'String',cell2mat(fit(11)));
    set(handles.out_FLW_R,'String',cell2mat(fit(12)));
